from abc import ABC, abstractmethod

class Clothes(ABC):
  def __init__(self, parameter):
    self.parameter = parameter
  @abstractmethod
  def calculate(self):
    pass

class Coat(Clothes):
  @property
  def calculate(self):
    return round((self.parameter / 6.5) + 0.5)

class Costume(Clothes):
  @property
  def calculate(self):
    return round((2 * self.parameter) + 0.3)

coat = Coat(100)
costume = Costume(100)

print(f'Ткани на пально: {coat.calculate}')
print(f'Ткани на косюм: {costume.calculate}')
print(f'Всего нужно ткани: {coat.calculate+costume.calculate}')

