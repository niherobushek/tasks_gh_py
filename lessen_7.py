class Matrix:

  def __init__(self, my_list):
    self.my_list = my_list

  def __str__(self):
    for row in self.my_list:
      for _ in row:
        print(f"{_:5}", end="")
      print()
    return ''

  def __add__(self, nex):
    for i_1 in range(len(self.my_list)):
      for i_2 in range(len(nex.my_list[i_1])):
        self.my_list[i_1][i_2] = self.my_list[i_1][i_2] + nex.my_list[i_1][i_2]
    return Matrix.__str__(self)


m1 = Matrix([[-1, 5, 1], [5, 0, 1], [0, -5, -1]])
